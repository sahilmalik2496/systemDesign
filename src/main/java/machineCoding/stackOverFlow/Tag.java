package machineCoding.stackOverFlow;

class Tag {
    private String tagId;
    private String name;

    public Tag(String tagId, String name) {
        this.tagId = tagId;
        this.name = name;
    }

    // Getters and setters
    public String getTagId() {
        return tagId;
    }

    public String getName() {
        return name;
    }
}
